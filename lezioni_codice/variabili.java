package javafiles;//Spiegazione di variabili e costanti

class variabili
{

	public static void main (String[]args)
	{
	 	//La dichiarazione di una variabile ha questa forma
		//tipo nomevariabile;
		//Se si volesse dichiarare una variabile intera
		int numero;
		float altezza = 1.1f; //f fa intendere che si tratta di un numero a singola precisione
		System.out.println(altezza);
		//Per dichiarare una costante si usa "final"
		final float pi = 3.14f;
		System.out.println(pi);
	
	}
}
